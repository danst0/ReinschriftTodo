static OVERRIDE_LANG: OnceLock<Mutex<Option<String>>> = OnceLock::new();

pub fn set_language(lang: String) {
    let m = OVERRIDE_LANG.get_or_init(|| Mutex::new(None));
    if let Ok(mut guard) = m.lock() {
        *guard = Some(lang);
    }
}

